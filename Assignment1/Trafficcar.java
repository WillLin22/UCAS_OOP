package Assignment1;

public class Trafficcar {
    int type;
    int license;
    public Trafficcar(int type, int license)
    {
        this.type = type;
        this.license = license;
    }
    int gettype()
    {
        return this.type;
    }
    int getlicense()
    {
        return this.license;
    }
}